num=int(input("enter first number: "))
num2=int(input("enter second number: "))
op=input("enter mathematical: ")
if op=="+":
    print("Result is: ",num+num2)
elif op=="*":
    print("Result is: ",num*num2)
elif op=="/":
    print("Result is: ",num/num2)
elif op=="%":
    print("Result is: ",num%num2)
elif op=="//":
    print("Result is: ",num//num2)
elif op=="-":
    print("Result is: ",num-num2)